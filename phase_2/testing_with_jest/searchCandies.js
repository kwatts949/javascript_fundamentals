const searchCandies = (string, maxPrice) => { 
  const filtered = []
  candies
    .filter(candy => candy.price < maxPrice)
    .filter(candy => candy.name.toUpperCase().startsWith(string.toUpperCase()))
    .map(candy => filtered.push(candy.name));
    return filtered;
}

module.exports = searchCandies;
